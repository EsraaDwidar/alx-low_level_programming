#include "3-calc.h"

/**
 * op_add - the function
 *
 * @a: first term
 * @b: second term
 *
 * Return: add
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - the function
 *
 * @a: first term
 * @b: second term
 *
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - the function
 *
 * @a: first term
 * @b: second term
 *
 * Return: mul
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - the function
 *
 * @a: first term
 * @b: second term
 *
 * Return: devision
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - the function
 *
 * @a: first term
 * @b: second term
 *
 * Return: module
 */

int op_mod(int a, int b)
{
	return (a % b);
}
